/** Helper: get subcategories for a given segment */
export function getSubcategoriesForSegment(segment: Segment): SubCategory[] {
  if (segment === 'Kids') return ['Girls', 'Boys', 'Babies'];
  return ['Clothing', 'Shoes', 'Accessories'];
}

/** Helper: count products */
export function getProductCount(segment: Segment, subcategory?: SubCategory): number {
  return PRODUCTS.filter(p => p.segment === segment && (!subcategory || p.subcategory === subcategory)).length;
}

